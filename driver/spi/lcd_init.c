#include "lcd_init.h"

#define GPIO_PATH "/sys/class/gpio"
#define LCD_DC_Clr()   { write(dc_fd, "0", 1); } 
#define LCD_DC_Set()   { write(dc_fd, "1", 1); } 
#define LCD_RES_Clr()  { write(res_fd, "0", 1); } 
#define LCD_RES_Set()  { write(res_fd, "1", 1); } 

static int spi_fd = -1;
static int dc_fd = -1;
static int res_fd = -1;

int init_gpio(int gpio) 
{
    char path[64];
    int fd = -1;

	// export
    {
		sprintf(path, "%s/export", GPIO_PATH);
		fd = open(path, O_WRONLY);
		if (fd < 0) {
			perror("open export for writing");
			return -1;
		}
		write(fd, &gpio, sizeof(gpio));
		close(fd);
	}
 
    // direction
    {
		sprintf(path, "%s/gpio%d/direction", GPIO_PATH, gpio);
		fd = open(path, O_RDWR);
		if (fd < 0) {
			perror("open direction for reading and writing");
			return -1;
		}
		write(fd, "out", 3);
		close(fd);
	}
 
    // value
    {
		sprintf(path, "%s/gpio%d/value", GPIO_PATH, gpio);
		fd = open(path, O_RDWR);
		if (fd < 0) {
			perror("open value for reading and writing");
			return -1;
		}
		write(fd, "1", 1);
	}
 
    return fd;
}

void closeLCD()
{
	close(dc_fd);
	close(res_fd);
	close(spi_fd);
}

void LCD_Writ_Bus(u8 dat) 
{	
	int ret = 0;
    unsigned char tx[] = {dat};
    unsigned char rx[sizeof(tx)] = {0};
    struct spi_ioc_transfer tr = {
        .tx_buf = (unsigned long)tx,
        .rx_buf = (unsigned long)rx,
        .len = sizeof(tx),
    };
 
    ret = ioctl(spi_fd, SPI_IOC_MESSAGE(1), &tr);
    if (ret < 1) {
        perror("can't send spi message");
    }
}

void LCD_WR_DATA8(u8 dat)
{
	LCD_Writ_Bus(dat);
}

void LCD_WR_DATA(u16 dat)
{
	LCD_Writ_Bus(dat>>8);
	LCD_Writ_Bus(dat);
}

void LCD_WR_REG(u8 dat)
{
	LCD_DC_Clr();
	LCD_Writ_Bus(dat);
	LCD_DC_Set();
}

void LCD_Address_Set(u16 x1,u16 y1,u16 x2,u16 y2)
{
	LCD_WR_REG(0x2a);
	LCD_WR_DATA(x1);
	LCD_WR_DATA(x2);
	LCD_WR_REG(0x2b);
	LCD_WR_DATA(y1);
	LCD_WR_DATA(y2);
	LCD_WR_REG(0x2c);
}

void delay_ms(int ms)
{
	usleep(ms * 1000);
}

